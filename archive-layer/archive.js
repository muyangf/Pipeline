/*
 * 智能归档引导壳（Archive Wrapper 2.0）
 *
 * 目标：
 * - 为归档操作提供结构化、中文化、离线友好的上层 Prompt
 * - 自动生成：变更摘要、影响范围、风险说明、归档记录
 * - 与 OpenSpec 的“变更规范（Change Spec）”保持一致
 * - 输出可直接用于 changelog 的内容
 *
 * 特点：
 * - 不依赖外部仓库
 * - 不执行实际文件写入（保持半自动）
 * - 完全离线可用
 */

module.exports = async ({ input }) => {
  return `
# 归档记录（Archive）

待归档内容：
${input}

---

请根据以上内容生成一份“归档记录（Archive Record）”，并严格遵守以下要求：

1. **变更摘要（中文）**
   - 用中文总结本次变更的核心内容
   - 必须包含：新增 / 修改 / 删除（如有）
   - 必须简洁、准确、可用于 changelog

2. **变更原因（中文）**
   - 为什么要做这次变更？
   - 必须包含：背景、动机、触发原因

3. **影响范围（中文）**
   - 本次变更影响哪些模块？
   - 是否影响业务逻辑？
   - 是否影响 AI 编排（Graph / Node / Tool）？
   - 是否影响测试？

4. **风险说明（中文）**
   - 列出潜在风险
   - 每条风险必须给出中文理由

5. **验证方式（中文）**
   - 如何确认本次变更是正确的？
   - 是否需要补充测试？

6. **归档记录（最终输出）**
   - 这是最终可写入 changelog 的内容
   - 必须结构化、中文、简洁

7. **下一步建议**
   - 通常是：\`/archive-layer.changelog\`
   - 必须中文说明理由

---

# 输出格式（必须严格遵守）

## 变更摘要
- ……

## 变更原因
- ……

## 影响范围
- ……

## 风险说明
1. ……
2. ……

## 验证方式
- ……

## 归档记录（最终输出）
- ……

## 下一步建议
- ……

所有内容必须使用中文。
`;
};
