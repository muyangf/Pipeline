/*
 * 智能完成分支引导壳（Finish Branch Wrapper 2.0）
 *
 * 目标：
 * - 为 Superpowers 的 finish-branch 能力提供结构化、中文化、离线友好的上层 Prompt
 * - 自动识别当前任务是否已完成 code-review / refactor
 * - 自动生成“分支完成报告”（变更摘要 / 风险说明 / 合并建议）
 * - 输出“下一步建议”（通常是 archive 或 changelog）
 *
 * 特点：
 * - 不直接实现 finish-branch 逻辑（底层由 Superpowers 完成）
 * - 这是“包装器（Wrapper）”，负责结构、规范、中文化、稳定性
 * - 完全离线可用
 */

module.exports = async ({ input }) => {
  return `
# 完成分支（Finish Branch）

待完成分支的上下文信息：
${input}

---

请根据以上上下文，对当前分支进行“完成分支（Finish Branch）”操作，并严格遵守以下要求：

1. **变更摘要（中文）**
   - 用中文总结本次分支的核心变更内容
   - 必须包含：新增内容、修改内容、删除内容（如有）
   - 必须简洁、准确、可用于 changelog

2. **风险评估（中文）**
   - 列出本次变更可能带来的风险
   - 包含：兼容性风险、逻辑风险、性能风险、AI 编排风险（如适用）
   - 每条风险必须给出中文理由

3. **合并建议（中文）**
   - 是否建议合并到主分支（是 / 否）
   - 如果否，必须说明原因
   - 如果是，必须说明合并后的注意事项

4. **代码质量确认（中文）**
   - 确认是否已经完成：
     - code-review
     - refactor（如需要）
     - 测试（如适用）
   - 如果缺失，请明确指出并给出补救建议

5. **完成分支后的动作建议（中文）**
   - 例如：
     - 建议执行 \`/archive-layer.archive\`
     - 建议执行 \`/archive-layer.changelog\`
   - 必须中文说明理由

6. **信息不足时必须先提问**
   - 如果上下文不足以完成分支，请先用中文说明你需要哪些补充信息

---

# 输出格式（必须严格遵守）

请严格按照以下结构输出：

## 变更摘要
- ……

## 风险评估
1. ……
2. ……
3. ……

## 合并建议
- 建议：合并 / 不合并
- 理由：……

## 代码质量确认
- code-review：已完成 / 未完成（理由）
- 重构（refactor）：已完成 / 未完成（理由）
- 测试：已完成 / 未完成（理由）

## 完成分支后的动作建议
- ……

所有内容必须使用中文。
`;
};
