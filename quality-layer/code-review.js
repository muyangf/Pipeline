/*
 * 智能代码审查引导壳（Code Review Wrapper 2.0）
 *
 * 目标：
 * - 为 Superpowers 的 code-review 能力提供结构化、中文化、离线友好的上层 Prompt
 * - 自动识别代码类型（业务代码 / AI 编排代码）
 * - 自动切换审查规范（business-code-style / ai-code-style）
 * - 输出结构化审查报告（问题列表 / 风险分析 / 改进建议 / 是否通过）
 * - 输出“下一步建议”，与 next-step 2.0 完美衔接
 *
 * 特点：
 * - 不直接实现审查逻辑（底层由 Superpowers 完成）
 * - 这是“包装器（Wrapper）”，负责结构、规范、中文化、稳定性
 * - 完全离线可用
 */

module.exports = async ({ input }) => {
  return `
# 代码审查（Code Review）

待审查代码：
\`\`\`
${input}
\`\`\`

---

请对以上代码进行全面的中文代码审查，并严格遵守以下要求：

1. **自动识别代码类型**
   - 如果包含 Graph / Node / Tool / Agent / State → 视为 AI 编排代码
   - 如果包含 Controller / Service / Repository → 视为业务代码
   - 根据识别结果自动切换审查规范：
     - /presets/ai-code-style.md
     - /presets/business-code-style.md

2. **输出结构化审查报告（必须中文）**
   - 问题列表（逐条列出）
   - 风险分析（为什么这些问题重要）
   - 改进建议（具体可执行）
   - 是否通过审查（通过 / 不通过）

3. **代码质量维度（必须覆盖）**
   - 可读性（命名、注释、结构）
   - 可维护性（模块化、职责划分）
   - 健壮性（边界情况、错误处理）
   - 一致性（是否符合智能 code-style-smart）
   - 架构合理性（业务代码 vs AI 编排代码）
   - 测试覆盖（是否需要补充测试）

4. **下一步建议**
   - 例如：需要重构、需要补测试、需要再次审查、可以归档
   - 必须中文说明理由

5. **信息不足时必须先提问**
   - 如果代码不完整或缺少上下文，请先用中文说明你需要哪些补充信息

---

# 输出格式（必须严格遵守）

请严格按照以下结构输出：

## 代码类型判断
- 类型：……
- 理由：……

## 问题列表
1. ……
2. ……
3. ……

## 风险分析
- ……

## 改进建议
- ……

## 审查结论
- 通过情况：通过 / 不通过
- 理由：……

## 下一步建议
- ……

所有内容必须使用中文。
`;
};
