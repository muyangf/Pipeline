/*
 * 智能重构引导壳（Refactor Wrapper 2.0）
 *
 * 目标：
 * - 为 Superpowers 的 refactor 能力提供结构化、中文化、离线友好的上层 Prompt
 * - 自动识别代码类型（业务代码 / AI 编排代码）
 * - 自动切换重构规范（business-code-style / ai-code-style）
 * - 输出结构化重构报告（问题 → 重构策略 → 重构后代码）
 * - 输出“下一步建议”，与 next-step 2.0 完美衔接
 *
 * 特点：
 * - 不直接实现重构逻辑（底层由 Superpowers 完成）
 * - 这是“包装器（Wrapper）”，负责结构、规范、中文化、稳定性
 * - 完全离线可用
 */

module.exports = async ({ input }) => {
  return `
# 代码重构（Refactor）

待重构代码：
\`\`\`
${input}
\`\`\`

---

请对以上代码进行结构化、中文化的重构，并严格遵守以下要求：

1. **自动识别代码类型**
   - 如果包含 Graph / Node / Tool / Agent / State → 视为 AI 编排代码
   - 如果包含 Controller / Service / Repository → 视为业务代码
   - 根据识别结果自动切换重构规范：
     - /presets/ai-code-style.md
     - /presets/business-code-style.md

2. **输出结构化重构报告（必须中文）**
   - 重构动机（为什么需要重构）
   - 发现的问题（逐条列出）
   - 重构策略（逐条说明）
   - 重构后的代码（完整代码块）
   - 重构前后对比（如适用）

3. **重构质量维度（必须覆盖）**
   - 可读性（命名、注释、结构）
   - 可维护性（模块化、职责划分）
   - 健壮性（边界情况、错误处理）
   - 一致性（是否符合智能 code-style-smart）
   - 架构合理性（业务代码 vs AI 编排代码）
   - 测试影响（是否需要更新测试）

4. **下一步建议**
   - 例如：是否需要再次 code-review、是否需要 finish-branch、是否需要 archive
   - 必须中文说明理由

5. **信息不足时必须先提问**
   - 如果代码不完整或缺少上下文，请先用中文说明你需要哪些补充信息

---

# 输出格式（必须严格遵守）

请严格按照以下结构输出：

## 代码类型判断
- 类型：……
- 理由：……

## 重构动机
- ……

## 发现的问题
1. ……
2. ……
3. ……

## 重构策略
- ……

## 重构后的代码
\`\`\`
（重构后的代码）
\`\`\`

## 重构前后对比（如适用）
- ……

## 下一步建议
- ……

所有内容必须使用中文。
`;
};
